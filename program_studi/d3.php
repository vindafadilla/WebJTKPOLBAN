        <h2>Misi Program Studi</h2>
        Menyelenggarakan program pendidikan diploma III Teknik Informatika yang diakui baik di tingkat nasional maupun internasional. <br><br>

        <h2>Tujuan Program Studi</h2>
          <ol>
            <li>Menghasilkan tenaga di bidang perancangan dan implementasi perangkat lunak bisnis serta perancangan solusi bisnis berbasis teknologi informasi untuk menunjang kebutuhan masyarakat dan industri di lingkup nasional dan internasional, yang memiliki sikap dan kemampuan sebagai berikut:</li>
              <ol>
                <li>beradaptasi terhadap perkembangan teknologi informasi</li>
                <li>belajar sepanjang hayat dan ulet</li>
                <li>berpikir kreatif, analitis dan sistematis</li>
                <li>berpikir kreatif, analitis dan sistematis</li>
                <li>bermoral</li>
                <li>berkomunikasi dalam bahasa Internasional</li>
              </ol>
            <li>Menghasilkan lulusan dengan kompetensi yang diakui pada tingkat nasional maupun internasional.</li>
            <li>Mendorong mahasiswa untuk menghasilkan produk terapan di bidang perangkat lunak bisnis yang bermanfaat bagi masyarakat dan industri baik nasional maupun internasional.</li>
          </ol>  
        <br>

        <h2>Profil Lulusan</h2>
        Lulusan program studi Teknik Informatika dibekali dengan pengetahuan teoritis dan pengalaman praktek sehingga siap masuk ke dunia kerja. Lulusan dikategorikan sebagai Informatic-workers yaitu profesional yang bekerja dibidang informatika atau bidang lain yang berbasis teknologi informatika sebagai operator, programmer, atau analis dan designer. Lulusan akan mampu bekerja pada bidang, misalnya seperti :<br>

        <ul>
          <li>Pengembangan Perangkat Lunak dan aplikasi</li>
          <li>Konsultan Industri Teknologi Informasi</li>
          <li>Pengembangan Perangkat Lunak multimedia</li>
          <li>Pemeliharaan Tenologi Jaringan</li>
        </ul>
