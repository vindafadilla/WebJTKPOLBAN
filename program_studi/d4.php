      <div class="panel-heading"><h2><b>D4 Teknik Informatika</b></h2></div>
      <div class="panel-body text-justify">
        Perkembangan perekonomian global secara positif telah menjadi tantangan dan peluang bagi semua negara termasuk Indonesia. Selaras dengan perkembangan industri khususnya dibidang Teknologi Informasi dan Komunikasi, serta kebijakan otonomi daerah di Indonesia, setiap institusi dituntut untuk mampu memanfaatkan teknologi dan kebijakan ini secara optimal. Oleh karena itu setiap institusi secara maksimal perlu mempersiapkan sumber daya manusianya sehingga memadai, baik dari segi kuantitas maupun kualitasnya.<br><br>

        Institusi pendidikan tinggi merupakan lembaga utama dalam menciptakan para calon praktisi dalam di industri.<br><br>

        Salah satu program pendidikan yang turut beperan adalah program pendidikan jalur vokasi. Politeknik Negeri Bandung sebagai lembaga pendidikan jalur vokasi memiliki potensi dan kesempatan yang memadai dalam mendukung tuntutan penyediaan sumber daya manusia (SDM) dibidang Teknologi Informasi dan Komunikasi.<br><br>

        Seiring dengan pemanfaatan perangkat lunak diberbagai bidang kehidupan dan KEPMEN 232/U/2000, maka Indonesia banyak memerlukan tenaga ahli yang mampu melaksanakan pekerjaan yang kompleks berdasarkan kemampuan profesional dibidang informatika.<br><br>

        Untuk itu, pada Juli 2009 didirikanlah program pendidikan D IV bidang informatika  di Jurusan Teknik Komputer dan Informatika  untuk menjawab kebutuhan terhadap penyediaan SDM yang berkaitan dengan produksi dan pemanfaatan perangkat lunak. Program studi ini dikukuhkan melalui SK Penyelenggaraan  Program Studi : dari Dikti dengan nomor 1265/D/T/2009.<br><br>

        <h2>Visi Program Studi</h2>
        Pada tahun 2020, menjadi program studi unggulan pada jenjang pendidikan diploma IV bidang Teknik Informatika ditingkat nasional bahkan internasional. Menjadi program studi yang menghasilkan tenaga profesional pengembang perangkat lunak yang kompeten, memiliki semangat terus berkembang, bermoral, berjiwa wirausaha, dan berwawasan lingkungan. <br><br>

        <h2>Misi Program Studi</h2>
        <ul>
          <li>Menyelenggarakan program pendidikan yang  menghasilkan sarjana terapan di bidang Rekayasa Perangkat Lunak (RPL) yang kompeten, bermoral, berjiwa wirausaha, berwawasan lingkungan serta mampu bersaing di tingkat nasional maupun internasional</li>
          <li>Melaksanakan penelitian dibidang Teknologi Informasi yang dapat diterapkan pada Industri atau masyarakat ditingkat nasional maupun internasional</li>
          <li>Melakukan pengabdian kepada masyarakat melalui diseminasi ilmu dan penerapannya di lingkup nasional.</li>
        </ul> <br>

        <h2>Tujuan Program Studi</h2>
        Tujuan D4 Teknik Informatika adalah menjalankan tridharma secara konsisten dan utuh sehingga menghasilkan lulusan yang mampu:<br>
          <ol>
            <li>Menunjukkan keunggulan keahlian dan pengetahuan, serta memiliki sikap profesionalisme yang dibutuhkan untuk menjadi seorang software designer dan analyst.</li>
            <li>Bekerja secara individu dan menjadi bagian dari suatu team untuk membangun, menyajikan dan memelihara perangkat lunak yang berkualitas.</li>
            <li>Mengelola proyek pembangunan perangkat lunak.</li>
          </ol>  
        <br>

        <h2>Profil Lulusan</h2>
        Lulusan program studi Teknik Informatika dibekali dengan pengetahuan teoritis dan pengalaman praktek sehingga siap masuk ke dunia kerja. Lulusan dikategorikan sebagai Informatic-workers yaitu profesional yang bekerja dibidang informatika atau bidang lain yang berbasis teknologi informatika sebagai operator, programmer, atau analis dan designer. Lulusan akan mampu bekerja pada bidang, misalnya seperti :<br>

        <ul>
          <li>Pengembangan Perangkat Lunak dan aplikasi</li>
          <li>Konsultan Industri Teknologi Informasi</li>
          <li>Pengembangan Perangkat Lunak multimedia</li>
          <li>Pemeliharaan Tenologi Jaringan</li>
        </ul>
        <br>

        <h2>Kualifikasi Dosen</h2>
        Dosen program studi ini berkualifikasi S2 dan S3  dari perguruan tinggi ternama dari dalam negeri (ITB, UI), maupun dari luar negeri (Inggris, Jepang, Amerika, Australia). Mayoritas dosen telah tersertifikasi (sertifikasi dosen).<br><br>

        <h2>Fasilitas Penunjang</h2>
        <ul>
          <li>Laboratorium Rekayasa Perangkat Lunak</li>
          <li>Laboratorium Sistem Informasi dan Basis Data</li>
          <li>Laboratorium Multimedia</li>
          <li>Laboratorium Jaringan dan Teknologi Informasi</li>
        </ul>

        <br>

        <h2>Prospek Lulusan</h2>
        Lulusan dari program studi ini berpotensi untuk bekerja pada kelompok bidang perkerjaan berikut :
        <ul>
          <li>Senior Analyst</li>
          <li>System Developer</li>
          <li>Software Testing Professional</li>
          <li>Manager IT</li>
        </ul>

